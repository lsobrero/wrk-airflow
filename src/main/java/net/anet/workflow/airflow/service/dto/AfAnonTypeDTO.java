package net.anet.workflow.airflow.service.dto;

public class AfAnonTypeDTO {
    private long id;
    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        AfAnonTypeDTO that = (AfAnonTypeDTO) o;

        return id == that.id;
    }

    @Override
    public int hashCode() {
        return (int) (id ^ (id >>> 32));
    }

    @Override
    public String toString() {
        return "AfAnonTypeDTO{" +
            "id=" + id +
            ", name='" + name + '\'' +
            '}';
    }
}
